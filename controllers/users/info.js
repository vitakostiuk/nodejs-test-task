// робить запит за токеном і перевіряється, що він дійсний
const info = async (req, res) => {
  // В req.user буде зберігатися інформація про користувача
  // Якщо токен буде невалідний, то мідлвара login поверне помилку 401
  const { id } = req.user;

  // Віддаємо на фронтент об'єкт з інформацією про карент юзера
  res.json({
    user: {
      id,
    },
  });
};

module.exports = info;
